TArray<USceneComponent*> APointCollection::GetGroundPoints(const FVector& GroundLocation, int32 NumPoints, float YawOverride)
{
	checkf(ImmutablePts.Num() >= NumPoints, TEXT("Attempted to access ImmutablePts out of bounds."));

	TArray<USceneComponent*> ArrayCopy;

	for (USceneComponent* Pt : ImmutablePts)
	{
		if (ArrayCopy.Num() >= NumPoints) return ArrayCopy;

		if (Pt != Pt_0)
		{
			FVector ToPoint = Pt->GetComponentLocation() - Pt_0->GetComponentLocation();  // Vector From Center to the point.
			ToPoint = ToPoint.RotateAngleAxis(YawOverride, FVector::UpVector); // Rotate this Point to Upwards.
			Pt->SetWorldLocation(Pt_0->GetComponentLocation() + ToPoint);
		}

		// Trace Start and end ,which will be lowered or raised locations,respectfully.

		const FVector RaisedLocation = FVector(Pt->GetComponentLocation().X, Pt->GetComponentLocation().Y, Pt->GetComponentLocation().Z + 500.f);
		const FVector LoweredLocation = FVector(Pt->GetComponentLocation().X, Pt->GetComponentLocation().Y, Pt->GetComponentLocation().Z - 500.f);

		FHitResult HitResult;
		TArray<AActor*> IgnoreActors;   //  The  All live players within radius that's basically anything that implements the combat interface that isn't dead. 
		UAuraAbilitySystemLibrary::GetLivePlayersWithinRadius(this, IgnoreActors, TArray<AActor*>(), 1500.f, GetActorLocation());

		FCollisionQueryParams QueryParams;
		QueryParams.AddIgnoredActors(IgnoreActors);
		GetWorld()->LineTraceSingleByProfile(HitResult, RaisedLocation, LoweredLocation, FName("BlockAll"), QueryParams);

		const FVector AdjustedLocation = FVector(Pt->GetComponentLocation().X, Pt->GetComponentLocation().Y, HitResult.ImpactPoint.Z);
		Pt->SetWorldLocation(AdjustedLocation);
		Pt->SetWorldRotation(UKismetMathLibrary::MakeRotFromZ(HitResult.ImpactNormal)); // MakeRotFromZ() will get a rotator from vector and Normal is the vector from straight ground perpendicular to that face it was hit.

			/*.... Now this doesn't mean that we're creating a copy of the scene component.

		 We're not.

		 There's only ten scene components here.

		We're not creating any new ones.

		We're just moving around those scene components.

		So they start off at their original positions, we move them and we return an array that contains some of them.

	   Not all of them, just some of them.

		Just enough for our needs determined by num points.

		And we're looping over all of these points starting at zero.

		And each time we're adding one to our array copy, and we're going to return as soon as our array copy

	   becomes equal to num points.

		 We said greater than we could use greater than or equal to here. Really.

		Because once this array contains num points we're ready to return it.

		 And then we can use those scene components in any way we want.

		 So now we can see how this works.

		 Of course, our get ground points needs to return an array.

		 So by the very end of this we have to actually return array copy.
		*/

		ArrayCopy.Add(Pt);
	}
	return ArrayCopy;
}

